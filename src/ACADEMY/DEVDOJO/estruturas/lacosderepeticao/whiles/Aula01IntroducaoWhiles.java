package ACADEMY.DEVDOJO.estruturas.lacosderepeticao.whiles;


//while , do while , for..


public class Aula01IntroducaoWhiles {
    public static void main(String[] args) {
        int contador = 0;
        while (contador <= 10){
            System.out.println("Contador: " + contador);
            contador = contador + 1;
        }
    }
}
